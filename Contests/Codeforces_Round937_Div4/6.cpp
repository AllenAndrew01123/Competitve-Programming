#include<bits/stdc++.h>
using namespace std;
int solve(int a,int b ,int c)
{
    int aar[a];
    int bar[b];
    int car[c];
    memset(aar,0,sizeof(aar));
    memset(bar,0,sizeof(bar));
    memset(car,0,sizeof(car));
    
}
int main()
{
    int t;cin>>t;
    while(t--)
    {
        int a,b,c;cin>>a>>b>>c;
        solve(a,b,c);
    }
}